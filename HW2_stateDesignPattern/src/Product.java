
public abstract class Product {
	private String productName;
	private int totalProductionDay;
	

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public int getTotalProductionDay() {
		return totalProductionDay;
	}

	public void setTotalProductionDay(int totalProductionDay) {
		this.totalProductionDay = totalProductionDay;
	}
}
